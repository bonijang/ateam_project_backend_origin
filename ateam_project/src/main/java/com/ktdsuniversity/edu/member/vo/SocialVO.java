public class SocialVO {
	private String socialId;
	private String socailEmail;
	private String socialType;
	private String socialNickname;
	private String deleteYn;
	
	public String getSocialId() {
		return socialId;
	}
	public void setSocialId(String socialId) {
		this.socialId = socialId;
	}
	public String getSocailEmail() {
		return socailEmail;
	}
	public void setSocailEmail(String socailEmail) {
		this.socailEmail = socailEmail;
	}
	public String getSocialType() {
		return socialType;
	}
	public void setSocialType(String socialType) {
		this.socialType = socialType;
	}
	public String getSocialNickname() {
		return socialNickname;
	}
	public void setSocialNickname(String socialNickname) {
		this.socialNickname = socialNickname;
	}
	public String getDeleteYn() {
		return deleteYn;
	}
	public void setDeleteYn(String deleteYn) {
		this.deleteYn = deleteYn;
	}
	
	
}
